public class ShapePoint {

    public float mX;
    public float mY;

    /**
     * ShapePoint
     */
    public ShapePoint() {
        mX = 0.0f;
        mY = 0.0f;
    }

    /**
     * ShapePoint
     * @param x x
     * @param y y
     */
    public ShapePoint(float x, float y) {
        mX = x;
        mY = y;
    }
}
